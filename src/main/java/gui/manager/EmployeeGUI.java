    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        emp.setFullname(txtName.getText());
        emp.setPhoneNumber(txtPhone.getText());
        emp.setAddress(new Address(txtAddress.getText(), "", "", ""));
        emp.setEmail(txtEmail.getText());
        emp.setPassword(txtPass.getText());
        emp.setRole(roleBUSImpl.findByName(cbbRole.getSelectedItem().toString()));
        emp.setActive(chkActive.isSelected());
        
        empBUS.updateEntity(emp);
        
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) { 

            tableModel.setValueAt(emp.getFullname(), selectedRow, 1);

            tableModel.setValueAt(emp.getPhoneNumber(), selectedRow, 3);
            tableModel.setValueAt(emp.getAddress(), selectedRow, 4);
            tableModel.setValueAt(emp.getEmail(), selectedRow, 5);
            tableModel.setValueAt(emp.getPassword(), selectedRow, 6);
            tableModel.setValueAt(emp.getRole().getRoleName(), selectedRow, 7);
            tableModel.setValueAt(emp.isActive() ? "true" : "false", selectedRow, 8);
            JOptionPane.showMessageDialog(null, "Cập nhật thành công");
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một hàng để cập nhật");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String name = txtName.getText();
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        String email = txtEmail.getText();
        String pass = txtPass.getText();
        RoleEntity role = roleBUSImpl.findByName(cbbRole.getSelectedItem().toString());
        boolean active = chkActive.isSelected();
        
        tableModel.setRowCount(0);
        List<EmployeeEntity> emps = empBUS.getEmployeesWithKeyword(name, phone, address, email, pass, role.getRoleId(), active);
        if(emps.isEmpty()) {
            emps = empBUS.getAllEntities();
        }
        for(EmployeeEntity emp : emps) {
            tableModel.addRow(new Object[] {emp.getEmployeeId(), emp.getFullname(), emp.getCreatedDate(), emp.getPhoneNumber(), 
                                emp.getAddress(), emp.getEmail(), emp.getPassword(), 
                                emp.getRole().getRoleName(), emp.isActive() ? "true" : "false"});
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int selectedRow = table.getSelectedRow();
        if(selectedRow == -1) {
            return;
        }
        String id = table.getValueAt(selectedRow, 0).toString();
        String name = table.getValueAt(selectedRow, 1).toString();
        String phone = table.getValueAt(selectedRow, 3).toString();
        String address = table.getValueAt(selectedRow, 4).toString();
        String email = table.getValueAt(selectedRow, 5).toString();
        String pass = table.getValueAt(selectedRow, 6).toString();
        String role = table.getValueAt(selectedRow, 7).toString();
        String active = table.getValueAt(selectedRow, 8).toString();

        txtId.setText(id);
        txtName.setText(name);
        txtPhone.setText(phone);
        txtAddress.setText(address);
        txtEmail.setText(email);
        txtPass.setText(pass);
        cbbRole.setSelectedItem(role);
        if(active.equals("true")) {
            chkActive.setSelected(true);
        }else {
            chkActive.setSelected(false);
        }

        this.emp = empBUS.getEntityById(id);
    }//GEN-LAST:event_tableMouseClicked


