    public void loadFloors() throws Exception {
        if (pnFloors.getComponentCount() == 0) {
            pnFloors.removeAll();
            
            floorBUS.getAllEntities().forEach(x -> {
                JRadioButtonCustom r = new JRadioButtonCustom(x.getName(), x);
                btnGroupFloor.add(r);
                if (btnGroupFloor.getButtonCount() == 1) {
                    rFirstFloor = r;
                }
                setRadioUI(r, pnFloors);
                radioActionPerform(r);
            });
            rFirstFloor.setSelected(true);
            
            if (btnGroupFloor.getSelection() == rFirstFloor.getButtonModel()) {
                JRadioButtonCustom firstButton = (JRadioButtonCustom) btnGroupFloor.getElements().nextElement();
                firstButton.setSelected(true);
                SwingUtilities.invokeLater(() -> firstButton.doClick());
            }
        }
        
        if (pnTableStatus.getComponentCount() == 0) {
            pnTableStatus.removeAll();
            String[] status = {"Tất cả", "Bàn trống", "Phục vụ", "Đặt trước"};
            for (String it : status) {
                JRadioButtonCustom r = new JRadioButtonCustom(it, it);
                btnGroupTableStatus.add(r);
                if (btnGroupTableStatus.getButtonCount() == 1) {
                    rFirstStatus = r;
                }
                setRadioUI(r, pnTableStatus);
                radioActionPerform(r);
            }
            
        }
        rFirstStatus.setSelected(true);
        if (btnGroupTableStatus.getSelection() == rFirstStatus.getButtonModel()) {
            JRadioButtonCustom firstButton = (JRadioButtonCustom) btnGroupTableStatus.getElements().nextElement();
            firstButton.setSelected(true);
            SwingUtilities.invokeLater(() -> firstButton.doClick());
        }
        ReloadComponentUlti.reload(panelTables);
        ReloadComponentUlti.reload(pnFloors);
        ReloadComponentUlti.reload(pnTableStatus);
    }
    
    private void setRadioUI(JRadioButtonCustom r, JPanel pn) {
        r.setPreferredSize(new Dimension(120, 42));
        r.setFont(new Font(r.getFont().getName(), Font.PLAIN, 18));
        pn.add(r);
        pn.add(Box.createRigidArea(new Dimension(30, 40)));
    }
    
    private void radioActionPerform(JRadioButtonCustom r) {
        r.addActionListener((ActionEvent e) -> {
            panelTables.removeAll();
            FloorEntity floor = (FloorEntity) getSelectedRadio(btnGroupFloor);
            String status = (String) getSelectedRadio(btnGroupTableStatus);
            try {
                loadTable(floor, status);
            } catch (Exception ex) {
                throw new RuntimeException(ex);
            }
            ReloadComponentUlti.reload(panelTables);
        });
    }
    
    private Object getSelectedRadio(ButtonGroup b) {
        ButtonModel btnModel = b.getSelection();
        JRadioButtonCustom selectedButton = getSelectedButton(b, btnModel);
        return selectedButton.getObject();
    }
    
    private void loadTable(FloorEntity floor, String status) throws Exception {
        List<TableEntity> tables = tableBUS.getListTablesByStatus(floor.getFloorId(), status);
        tables.forEach(x -> {
            try {
                loadPanelTables(x);
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        });
    }
    
    public void loadPanelTables(TableEntity table) throws Exception {
        PanelTable pnTable = new PanelTable(table, orderGUI);
        panelTables.setLayout(new FlowLayout(FlowLayout.LEFT, 30, 50));
        panelTables.add(pnTable);
    }

