    private void customTable() {
        String[] headers = {"Tên sản phẩm", "Số lượng", "Giảm giá", "Tổng tiền", "Thành tiền"};
        List<Integer> tableWidth = Arrays.asList(100, 100, 100, 100, 100);
        tableDesign = new TableDesign(headers, tableWidth);
        table = tableDesign.getTable();
        tableModel = tableDesign.getModelTable();
        columnModel = tableDesign.getColumnModel();
        table.setModel(tableModel);
        table.setColumnModel(columnModel);
        table.setRowHeight(50);
    }
    
    private void fillContent() {
        lblOrderId.setText(order.getOrderId());
        Set<OrderDetailEntity> orderDetails = order.getOrderDetails();
        for (OrderDetailEntity orderDetail : orderDetails) {
            tableModel.addRow(new Object[]{orderDetail.getItem().getName(), 
                orderDetail.getQuantity(), 
                orderDetail.getDiscount(), 
                orderDetail.getLineTotal(), 
                orderDetail.getLineTotal() - orderDetail.getDiscount()});
        }
    }

