    private void switchPanel(JPanel currentPanel, JPanel followingPanel) {
        currentPanel.removeAll();
        followingPanel.setBounds(0, 0, currentPanel.getWidth(), currentPanel.getHeight());
        currentPanel.add(followingPanel);
        currentPanel.revalidate();
        currentPanel.repaint();
    }

