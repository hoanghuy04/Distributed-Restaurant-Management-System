    private void btnContinue3ActionPerformed(java.awt.event.ActionEvent evt) throws Exception {//GEN-FIRST:event_btnContinue3ActionPerformed
        String password = txtPassword.getText().trim();
        String confirmPassword = txtPasswordConfirm.getText().trim();

        if (password.equalsIgnoreCase(confirmPassword)) {
            emp.setPassword(employeeBUS.hashPassword(password));
            emp = employeeBUS.updateEntity(emp);
            this.dispose();
            this.loginGUI.setTxtName(emp.getEmployeeId());
            this.loginGUI.setTxtPass(password);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng kiểm tra lại xác nhận mật khẩu!");
        }
    }//GEN-LAST:event_btnContinue3ActionPerformed

