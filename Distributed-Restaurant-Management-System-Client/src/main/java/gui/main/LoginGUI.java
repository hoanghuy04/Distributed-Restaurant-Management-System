    private void encryptPassword() throws RemoteException {
        while (empBUS == null) {
            System.out.println("EmpBus null");
        }

        List<EmployeeEntity> list = empBUS.getAllEntities(); // Thêm dấu ngoặc tròn
        for (EmployeeEntity e : list) {
            if (!isBCryptHash(e.getPassword())) { // Kiểm tra định dạng BCrypt
                e.setPassword(empBUS.hashPassword(e.getPassword())); // Mã hóa mật khẩu
                e = empBUS.updateEntity(e); // Cập nhật vào DB
            }
        }
    }

// Phương thức kiểm tra định dạng BCrypt
    private boolean isBCryptHash(String password) {
        // BCrypt hash thường bắt đầu bằng $2a$, $2b$, hoặc $2y$ và có độ dài chuẩn là 60 ký tự
        return password != null && password.matches("^\\$2[ayb]\\$.{56}$");
    }

