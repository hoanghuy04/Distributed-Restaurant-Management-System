    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) throws Exception {//GEN-FIRST:event_btnLoginActionPerformed
        String name = txtName.getText().trim();
        String id = new String(txtPass.getPassword());
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "Nhập tên đăng nhập");
            txtName.requestFocus();
            return;
        }
        if (id.length() == 0) {
            JOptionPane.showMessageDialog(this, "Nhập mật khẩu");
            txtPass.requestFocus();
            return;
        }
        emp = empBUS.checkLogin(name, id);
        if (emp != null) {
            FlatRobotoFont.install();
            FlatLaf.registerCustomDefaultsSource("theme");
            UIManager.put("defaultFont", new Font(FlatRobotoFont.FAMILY, Font.PLAIN, 20));
            FlatLightLaf.setup();
            if (emp.getRole().getRoleName().equals("STAFF")) {
                Application.app = new Application(1);
                Application.showForm(new MainGUI(Application.app));
                Application.app.setVisible(true);
            } else if (emp.getRole().getRoleName().equals("MANAGER")) {
                Application.app = new Application(2);
                Application.showForm(new OverviewGUI());
                Application.app.setVisible(true);
            }
            this.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(this, "Tài khoản hoặc mật khẩu sai");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

